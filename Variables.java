public class Variables
{
   public static void main( String[] args )
   {
      double x, result;
      x = 0.0;
      result = 3 * (x * x) - (8 * x) + 4;
      System.out.println("At x = 0.0 the value is " + result);
      
      x = 2.0;
      result = 3 * (x * x) - (8 * x) + 4;
      System.out.println("At x = 0.0 the value is " + result);
      
      x = 4.0;
      result = 3 * (x * x) - (8 * x) + 4;
      System.out.println("At x = 0.0 the value is " + result);
      
   }
}
